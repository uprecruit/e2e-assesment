// This is a sign in command. It's resuable and can be accessed from anywhere 
// Username and password are passed into this function
Cypress.Commands.add('signIn', (username,password) => {
    cy.get('#input-14').type(username);
    cy.get('#input-18').type(password);
    cy.get('.v-btn__content').click();
  })